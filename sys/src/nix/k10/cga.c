static int
cgaregr(int index)
{
	outb(Index, index);
	return inb(Data) & 0xff;
}

static void
cgaregw(int index, int data)
{
	outb(Index, index);
	outb(Data, data);
}

static void
cgacursor(void)
{
	uchar *cga;

	cgaregw(0x0e, (cgapos/2>>8) & 0xff);
	cgaregw(0x0f, cgapos/2 & 0xff);

	cga = CGA;
	cga[cgapos+1] = Attr;
}

static void
cgaputr(Rune c)
{
	int i;
	uchar *cga, *p;

	cga = CGA;

	if(c == '\n'){
		cgapos = cgapos/Width;
		cgapos = (cgapos+1)*Width;
	}
	else if(c == '\t'){
		i = 8 - ((cgapos/2)&7);
		while(i-- > 0)
			cgaputr(' ');
	}
	else if(c == '\b'){
		if(cgapos >= 2)
			cgapos -= 2;
		cgaputr(' ');
		cgapos -= 2;
	}
	else{
		if(c < ' ' || c >= 127)
			for(i = 0;; i++){
				if(i == nelem(ibm437)){
					c = 254;
					break;
				}
				if(ibm437[i] == c){
					c = i;
					break;
				}
			}
		cga[cgapos++] = c;
		cga[cgapos++] = Attr;
	}
	if(cgapos >= (Width*Height)-Postlen*2){
		memmove(cga, &cga[Width], Width*(Height-1));
		p = &cga[Width*(Height-1)-Postlen*2];
		for(i = 0; i < Width/2; i++){
			*p++ = ' ';
			*p++ = Attr;
		}
		cgapos -= Width;
	}
	cgacursor();
}

void
cgaconsputs(char* s, int n)
{
	int i;
	Rune r;

	ilock(&cgalock);
	while(n > 0 && fullrune(s, n)){
		i = chartorune(&r, s);
		n -= i;
		s += i;
		cgaputr(r);
	}
	iunlock(&cgalock);
}

void
cgapost(int code)
{
	uchar *cga;

	static char hex[] = "0123456789ABCDEF";

	cga = CGA;
	cga[Width*Height-Postcodelen*2] = hex[(code>>4) & 0x0f];
	cga[Width*Height-Postcodelen*2+1] = Attr;
	cga[Width*Height-Postcodelen*2+2] = hex[code & 0x0f];
	cga[Width*Height-Postcodelen*2+3] = Attr;
}

void
cgainit(void)
{
	ilock(&cgalock);
	cgapos = cgaregr(0x0e)<<8;
	cgapos |= cgaregr(0x0f);
	cgapos *= 2;
	cgainitdone = 1;
	iunlock(&cgalock);
}
